/**
 * 去除拼音声调，返回无声调拼音
 */
function stripTone(pinyin) {
  if (!pinyin) return '';
  return pinyin.replace(/[āáǎàēéěèīíǐìōóǒòūúǔùǖǘǚǜ]/g, function(match) {
    var map = {
      'ā': 'a', 'á': 'a', 'ǎ': 'a', 'à': 'a',
      'ē': 'e', 'é': 'e', 'ě': 'e', 'è': 'e',
      'ī': 'i', 'í': 'i', 'ǐ': 'i', 'ì': 'i',
      'ō': 'o', 'ó': 'o', 'ǒ': 'o', 'ò': 'o',
      'ū': 'u', 'ú': 'u', 'ǔ': 'u', 'ù': 'u',
      'ǖ': 'v', 'ǘ': 'v', 'ǚ': 'v', 'ǜ': 'v'
    };
    return map[match] || match;
  });
}

/**
 * 获取拼音的声母和韵母
 * 返回 { initial, final }
 */
function parsePinyin(pinyin) {
  if (!pinyin) return { initial: '', final: '' };
  var stripped = stripTone(pinyin).toLowerCase();
  // 零声母拼音：a, o, e 开头
  if (/^[aeo].*/.test(stripped)) {
    return { initial: '', final: stripped };
  }
  // 双声母：zh, ch, sh
  if (/^(zh|ch|sh)/.test(stripped)) {
    return { initial: stripped.substring(0, 2), final: stripped.substring(2) };
  }
  // 单声母
  return { initial: stripped.charAt(0), final: stripped.substring(1) };
}

/**
 * 判断两个拼音是否音近
 * 规则：
 * 1. 同声母+同韵母，不同声调 → 音近
 * 2. 声母相近（平翘舌z/zh, c/ch, s/sh, 鼻音n/l）+同韵母 → 音近
 * 3. 同声母+韵母相近（前鼻音an/en/in vs 后鼻音ang/eng/ing）→ 音近
 */
function isSoundSimilar(pinyin1, pinyin2) {
  if (!pinyin1 || !pinyin2) return false;
  if (pinyin1 === pinyin2) return false; // 完全相同不算音近

  var s1 = stripTone(pinyin1).toLowerCase();
  var s2 = stripTone(pinyin2).toLowerCase();
  if (s1 === s2) return true; // 无声调相同 = 音近

  var p1 = parsePinyin(pinyin1);
  var p2 = parsePinyin(pinyin2);

  // 规则1: 同声母+同韵母，无声调也相同(已在上面返回true)，这里检查韵母细微差异
  if (p1.initial === p2.initial && p1.final === p2.final) {
    return true;
  }

  // 规则2: 平翘舌相近 (z-zh, c-ch, s-sh)
  var flatCurlyPairs = { 'z': 'zh', 'zh': 'z', 'c': 'ch', 'ch': 'c', 's': 'sh', 'sh': 's' };
  if (flatCurlyPairs[p1.initial] === p2.initial && p1.final === p2.final) {
    return true;
  }

  // 规则3: n-l 不分
  if ((p1.initial === 'n' && p2.initial === 'l') || (p1.initial === 'l' && p2.initial === 'n')) {
    if (p1.final === p2.final) return true;
  }

  // 规则4: 前/后鼻音相近 (an-ang, en-eng, in-ing)
  var nasalPairs = { 'an': 'ang', 'ang': 'an', 'en': 'eng', 'eng': 'en', 'in': 'ing', 'ing': 'in' };
  if (p1.initial === p2.initial && nasalPairs[p1.final] === p2.final) {
    return true;
  }

  return false;
}

/**
 * 分类错误类型
 * @param {string} correctChar - 正确汉字
 * @param {string} correctPinyin - 正确拼音
 * @param {string} selectedChar - 用户选择的错误汉字（可选）
 * @param {string} selectedPinyin - 用户选择的错误拼音（可选）
 * @returns {{ errorType: string, similarChar: string, hint: string }}
 */
function classifyError(correctChar, correctPinyin, selectedChar, selectedPinyin) {
  var result = {
    errorType: 'general',
    similarChar: '',
    hint: ''
  };

  // 1. 检查形近
  if (selectedChar && SHAPE_SIMILAR_MAP[correctChar]) {
    var similarList = SHAPE_SIMILAR_MAP[correctChar];
    if (similarList.indexOf(selectedChar) !== -1) {
      result.errorType = 'shape_similar';
      result.similarChar = selectedChar;
      result.hint = '注意区分「' + correctChar + '」和「' + selectedChar + '」的偏旁部首';
      return result;
    }
  }

  // 2. 检查音近
  if (selectedPinyin && correctPinyin && isSoundSimilar(correctPinyin, selectedPinyin)) {
    result.errorType = 'sound_similar';
    result.similarChar = selectedChar || '';
    result.hint = '注意辨别发音相近的字，多听多辨';
    return result;
  }

  // 3. 没有选中的字但有正确拼音，尝试只判断拼音选项中的音近
  if (selectedChar && correctPinyin) {
    // 无法确定选中字的拼音，只检查形近
    result.hint = '多复习几遍，加强记忆';
    return result;
  }

  // 4. 无法归类 → general
  result.hint = '多复习几遍，加强记忆';
  return result;
}

/**
 * 获取定向巩固建议
 * @param {string} errorType - 错误类型
 * @param {string} similarChar - 混淆字
 * @returns {string} 巩固建议文案
 */
function getReinforcementHint(errorType, similarChar) {
  switch (errorType) {
    case 'shape_similar':
      return similarChar
        ? '形近字混淆 — 建议对比字形差异，重点区分偏旁部首'
        : '形近字混淆 — 建议对比字形差异';
    case 'sound_similar':
      return '读音混淆 — 建议多听发音，注意声调区别';
    case 'stroke':
      return '笔顺错误 — 建议多练习描红，熟悉笔画顺序';
    default:
      return '建议多复习几次，加深印象';
  }
}

module.exports = {
  classifyError: classifyError,
  getReinforcementHint: getReinforcementHint,
  isSoundSimilar: isSoundSimilar,
  stripTone: stripTone,
  SHAPE_SIMILAR_MAP: SHAPE_SIMILAR_MAP
};
